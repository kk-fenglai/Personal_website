export function extractFirstImageUrl(content: string): string | null {
  const md = content.match(/!\[[^\]]*\]\(([^)]+)\)/);
  if (md?.[1]) return md[1];
  const html = content.match(/<img[^>]+src=["']([^"']+)["']/i);
  return html?.[1] ?? null;
}

export function plainTextExcerpt(content: string, max = 220): string {
  const text = content
    .replace(/!\[[^\]]*\]\([^)]+\)/g, "")
    .replace(/<[^>]+>/g, "")
    .replace(/[#>*_`~-]/g, "")
    .replace(/\s+/g, " ")
    .trim();
  if (text.length <= max) return text;
  return `${text.slice(0, max).trim()}…`;
}
